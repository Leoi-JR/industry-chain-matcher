# 当前激活的产业链类型
ACTIVE_INDUSTRY = 'example'


def get_active_prompts():
    """
    获取当前激活的产业链提示词模板
    
    Returns:
        dict: 包含当前产业链的所有提示词模板
    """
    if ACTIVE_INDUSTRY not in PROMPT_TEMPLATES:
        raise ValueError(f"未找到产业链类型 '{ACTIVE_INDUSTRY}' 的提示词模板")
    
    return PROMPT_TEMPLATES[ACTIVE_INDUSTRY]


def build_user_prompt(l0, full_path, template=None):
    """
    构建用户提示词
    
    Args:
        l0: 主产业链名称 (L0)
        full_path: 完整产业链路径
        template: 用户提示词模板，如果为None则使用当前激活的模板
        
    Returns:
        str: 格式化后的用户提示词
    """
    if template is None:
        template = get_active_prompts()['user_prompt']
    
    return template.format(l0=l0, full_path=full_path)


